''' 3. Faça um programa que solicita a digitação
de uma medida em METROS. Calcule e mostre quanto
vale a medida em CENTÍMETROS e em MILÍMETROS '''

metros = float( input("Medida em METROS: ") )

centimetros = metros * 100
print("Medida em centímetros:", centimetros)

milimetros = metros * 1000
print("Medida em milímetros:", milimetros)

